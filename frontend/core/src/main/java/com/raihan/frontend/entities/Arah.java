package com.raihan.frontend.entities;

public class Arah {
    public boolean atas;
    public boolean bawah;
    public boolean kanan;
    public boolean kiri;

    public Arah(boolean atas, boolean bawah, boolean kanan, boolean kiri){
        this.atas = atas;
        this.bawah = bawah;
        this.kanan = kanan;
        this.kiri = kiri;
    }
}
